#ifndef PROJECT_TEMPERATURE_CONVERSION_H_
#define PROJECT_TEMPERATURE_CONVERSION_H_

/**
 * @NAME: FarenheitToCelcius
 * @DESC: Translates the temperature from Farenheit to Celcius
 * @float farenheit: Number of degrees in Farenheit
 */
float FarenheitToCelcius(float farenheit);

/**
 * @NAME: CelciusToFarenheit
 * @DESC: Translates the temperature from Celcius to Farenheit
 * @float celcius: Number of degrees in Celcius
 */
float CelciusToFarenheit(float celcius);

#endif